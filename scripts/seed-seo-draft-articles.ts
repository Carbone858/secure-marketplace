async function seedDraftArticles() {
  console.log('--- SEEDING 10 SEO DRAFT ARTICLES FOR REVIEW ---');
  let createdCount = 0;

  for (const article of draftArticles) {
    try {
      const existing = await prisma.cMSPage.findUnique({
        where: { slug: article.slug },
      });

      if (!existing) {
        await prisma.cMSPage.create({
          data: {
            slug: article.slug,
            title: article.title,
            titleAr: article.titleAr,
            content: article.contentAr,
            contentAr: article.contentAr,
            metaDescription: article.metaDescription,
            metaKeywords: article.metaKeywords,
            isPublished: false, // DRAFT MODE FOR USER REVIEW!
          },
        });
        createdCount++;
        console.log(`[DRAFT CREATED]: ${article.slug} (${article.titleAr})`);
      } else {
        console.log(`[EXISTS]: ${article.slug}`);
      }
    } catch (err) {
      console.error(`Error creating draft article ${article.slug}:`, err);
    }
  }

  console.log(`\nDONE! Created ${createdCount} draft articles (set to isPublished: false for review).`);
}

seedDraftArticles()
